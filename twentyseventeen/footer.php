<script>
/*
 * REGRA TEMPORARIA (ROLLBACK FACIL)
 *
 * Objetivo:
 * - Quando houver cards com classe .box-item,
 * - e o card for da modalidade Digital (EaD),
 * - ocultar APENAS o texto .ofertaCupom dentro desse card.
 *
 * Como a modalidade e os cards podem ser alterados dinamicamente por outros scripts,
 * esta rotina reaplica a regra no carregamento e em mutacoes do DOM.
 *
 * Rollback:
 * - Remover todo o bloco entre:
 *   INICIO BLOCO TEMPORARIO: ocultar cupom em cards Digital (EaD)
 *   FIM BLOCO TEMPORARIO: ocultar cupom em cards Digital (EaD)
 */
(function() {
    function normalizar(valor) {
        return (valor || '')
            .toString()
            .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
            .toLowerCase()
            .trim();
    }

    function isCardDigital(box) {
        if (!box) return false;

        // Preferencia: atributo estrutural usado na vitrine.
        var modalidadeAttr = normalizar(box.getAttribute('data-modalidade'));
        if (modalidadeAttr === 'digital') {
            return true;
        }

        // Fallback: texto visual da categoria no card.
        var categoriaEl = box.querySelector('.categoria');
        var categoriaTxt = normalizar(categoriaEl ? categoriaEl.textContent : '');
        return categoriaTxt === 'digital (ead)' || categoriaTxt === 'digital(ead)' || categoriaTxt === 'digital';
    }

    function aplicarRegraOcultarCupomDigital() {
        document.querySelectorAll('.box-item').forEach(function(box) {
            if (!isCardDigital(box)) {
                return;
            }

            box.querySelectorAll('.ofertaCupom').forEach(function(el) {
                el.style.display = 'none';
            });
        });
    }

    function iniciar() {
        aplicarRegraOcultarCupomDigital();

        var observer = new MutationObserver(function() {
            aplicarRegraOcultarCupomDigital();
        });

        observer.observe(document.body, {
            childList: true,
            subtree: true,
            attributes: true,
            attributeFilter: ['class', 'data-modalidade']
        });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', iniciar);
    } else {
        iniciar();
    }
})();
</script>
